function toggleMenu() {
    var menu = document.getElementById("menu");  // Obtén el menú
    menu.classList.toggle("show");  // Añade o elimina la clase 'show' para mostrar/ocultar el menú
}

function toggleDown() {
    var download = document.getElementById("download");  // Obtén el menú de descarga
    download.classList.toggle("show");  // Añade o elimina la clase 'show' para mostrar/ocultar el menú de descarga
}
